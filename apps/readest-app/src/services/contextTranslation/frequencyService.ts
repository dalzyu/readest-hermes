// Lookup tables per language
const EN_LEVELS: [Set<string>, string][] = [
  [EN_A1, 'A1'],
  [EN_A2, 'A2'],
  [EN_B1, 'B1'],
  [EN_B2, 'B2'],
];

const JA_LEVELS: [Set<string>, string][] = [
  [JA_N5, 'N5'],
  [JA_N4, 'N4'],
  [JA_N3, 'N3'],
  [JA_N2, 'N2'],
  [JA_N1, 'N1'],
];

const ZH_LEVELS: [Set<string>, string][] = [
  [ZH_HSK1, 'HSK 1'],
  [ZH_HSK2, 'HSK 2'],
  [ZH_HSK3, 'HSK 3'],
];

function lookupInLevels(word: string, levels: [Set<string>, string][]): FrequencyBadge | null {
  for (const [wordSet, level] of levels) {
    if (wordSet.has(word)) return { level };
  }
  return null;
}

/**
 * Returns a proficiency-level badge for the given word in the specified language.
 * Returns null if the word is not in the frequency database.
 */
export function getFrequencyBadge(word: string, language: string): FrequencyBadge | null {
  const normalized = word.toLowerCase().trim();
  if (!normalized) return null;

  switch (language) {
    case 'en':
      return lookupInLevels(normalized, EN_LEVELS);
    case 'ja':
      return lookupInLevels(normalized, JA_LEVELS);
    case 'zh':
      return lookupInLevels(normalized, ZH_LEVELS);
    default:
      return null;
  }
}
